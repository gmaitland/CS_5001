# For loop
# Not inclusive
# Used for known ranges, a lot easier than the managment of a while loop
# pass by value?
# Only square brackets [] are able to mutate a list * IMPORTANT *

def main():

    for i in range(50, 700, 5):
        # 1st Value Start, 2nd Value End, 3rd value Increment
        print(i)

# Can have 3 parameters
 
if __name__ == "__main__":
    main()


